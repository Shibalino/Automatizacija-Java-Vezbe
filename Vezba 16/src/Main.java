//    Kreirati "for" petlju koja ce prikazati tablicu mnozenja sa 1, 2 i 3 za brojeve od 1 do 10.


public class Main {
    public static void main(String[] args) {

        for (int i = 1; i < 11; i++) {
            System.out.println(i * 1 + " " + i * 2 + " " + i * 3);
        }
    }
}



